const INTRO_TAILS = [
  '值得为它留出整段假期。',
  '慢慢走才不负这趟远行。',
  '把它排进行程不会错。',
  '专程一来并不冤枉。',
  '留几天给它最划算。',
  '读懂它需要一点耐心。',
];
function fitIntro(d, city) {
  d = String(d || '').trim();
  if (countChars(d) >= 60) return d.slice(0, 110);
  for (let k = 0; k < INTRO_TAILS.length; k++) {
    if (countChars(d) >= 60) break;
    const t = INTRO_TAILS[k];
    if (d.includes(t.slice(0, 5))) continue;
    d += t;
  }
  if (countChars(d) > 110) d = d.slice(0, 110);
  return d;
}

// ---------------------------------------------------------------------------
// 主流程
// ---------------------------------------------------------------------------
const raw = fs.readFileSync(FILE, 'utf8');
const data = JSON.parse(raw);

const citiesObj = data.cities;
const cityKeys = Object.keys(citiesObj);
let cityCount = 0;
let attrCount = 0;

cityKeys.forEach((ck) => {
  const city = citiesObj[ck];
  if (!city || typeof city !== 'object') return;
  cityCount++;

  const kb = KB[city.name];

  // 城市 seoIntro
  if (kb && kb.intro) {
    city.seoIntro = fitIntro(kb.intro, city);
  } else {
    city.seoIntro = fitIntro(fallbackIntro(city), city);
  }
  checkLen(`${city.name}.seoIntro`, city.seoIntro, LIM.seoIntro);

  // 城市 faqs
  if (kb && Array.isArray(kb.faqs) && kb.faqs.length) {
    city.faqs = kb.faqs.map((f) => ({
      q: String(f.q || ''),
      a: String(f.a || ''),
    }));
  } else {
    city.faqs = fallbackFaqs(city);
  }
  if (!Array.isArray(city.faqs) || city.faqs.length !== 3) {
    warnings++;
    console.warn(`  [结构告警] ${city.name}.faqs 条数=${city.faqs ? city.faqs.length : 0}（期望3）`);
  }
  city.faqs.forEach((f, i) => checkLen(`${city.name}.faqs[${i}].a`, f.a, LIM.faqA));

  // 景点
  if (Array.isArray(city.attractions)) {
    city.attractions.forEach((a) => {
      if (!a || typeof a !== 'object') return;
      attrCount++;
      const ak = kb && kb.attractions && kb.attractions[a.name];

      if (ak && ak.seoDesc) {
        a.seoDesc = fitDesc(ak.seoDesc, a);
      } else {
        a.seoDesc = fitDesc(fallbackSeoDesc(a), a);
      }
      checkLen(`${city.name}/${a.name}.seoDesc`, a.seoDesc, LIM.seoDesc);

      if (ak && Array.isArray(ak.tips) && ak.tips.length >= 2) {
        a.tips = ak.tips.map((t, i) => fitTip(t, a, i));
      } else {
        a.tips = fallbackTips(a).map((t, i) => fitTip(t, a, i));
      }
      if (!Array.isArray(a.tips) || a.tips.length < 2 || a.tips.length > 3) {
        warnings++;
        console.warn(`  [结构告警] ${city.name}/${a.name}.tips 条数=${a.tips ? a.tips.length : 0}（期望2~3）`);
      }
      a.tips.forEach((t, i) => checkLen(`${city.name}/${a.name}.tips[${i}]`, t, LIM.tip));
    });
  }
});

fs.writeFileSync(FILE, JSON.stringify(data, null, 2), 'utf8');

console.log(`\n完成:增强城市 ${cityCount} 个,景点 ${attrCount} 个。`);
console.log(warnings === 0 ? '长度与结构检查:全部通过 ✓' : `长度/结构告警共 ${warnings} 条(见上,可作微调参考)。`);
